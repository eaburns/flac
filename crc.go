func verifyCRC16(data []byte) error {
	crc := uint16(0)
	for _, d := range data {
		crc = ((crc << 8) ^ crc16Table[(uint8(crc>>8)^d)]) & 0xFFFF
	}
	if crc == 0 {
		return nil
	}
	return errors.New("Bad checksum")
}
